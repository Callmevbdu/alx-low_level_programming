#include "3-calc.h"

/**
 * op_add - function that adds two numbers
 * @a: first input
 * @b: second input
 * Return: sum of calculation
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that substracts two numbers
 * @a: first input
 * @b: second input
 * Return: sum of calculation
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that multiplies two numbers
 * @a: first input
 * @b: second input
 * Return: sum of calculation
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that divides two numbers
 * @a: first input
 * @b: second input
 * Return: sum of calculation
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that modulo two numbers
 * @a: first input
 * @b: second input
 * Return: sum of calculation
*/

int op_mod(int a, int b)
{
	return (a % b);
}
